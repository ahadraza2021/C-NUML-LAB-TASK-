#include<iostream>
using namespace std;
int main()
{
	int num=57;
	num++;
	cout<<"post increment by 1 the number is:"<<num<<endl;
	++num;
	cout<<"pre increment by 1 the number is:"<<num<<endl;
	num=num+1;
	cout<<"after increasing by 1 the number is:"<<num<<endl;
	
	num--;
	cout<<"post decrement by 1 the number is:"<<num<<endl;
	--num;
	cout<<"pre decrement by 1 the number is:"<<num<<endl;
	num=num-1;
	cout<<"after decreasing by 1 the number is:"<<num<<endl;
	
}
